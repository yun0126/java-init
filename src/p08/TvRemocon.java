package p08;

public class TvRemocon extends Remocon {

	
	public void on() {
		System.out.println("TV를 킵니다");
		
	}

	
	public void off() {
		System.out.println("TV를 끕니다");
		
	}
	
	
	public void ChUp() {
		System.out.println("채널을 올립니다");
		
	}
	public void ChDown() {
		System.out.println("채널을 내립니다");
		
	}

}
